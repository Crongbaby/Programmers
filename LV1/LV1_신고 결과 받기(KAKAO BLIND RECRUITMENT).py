def solution(id_list, report, k):
  report = set(report) #중복 제거
  result = {i:0 for i in id_list}  #메일 보낼 횟수
  reported = {i:0 for i in id_list}  #신고 당한 횟수

  for i in report:
    reported[i.split()[1]] += 1

  for i in report:
    if reported[i.split()[1]] >= k:
      result[i.split()[0]] += 1

  return list(result.values())

